#variable declarations
#type() functions returns the type of given variable

#integer value
num1 = 123
print('Type:',type(num1))

#float value
num2 = 123.123
print('Type:',type(num2))

#string value
word = 'variable'
print('Type:',type(word))

#boolean value
var1 = True
var2 = False
print('Type:',type(var1))
print('Type:',type(var2))

#anything can be assigned to anything, that's the beauty of python
word = 123
num1 = 'hey!!'
num2 = True

#you can check the type of variables too, after inter-assigning